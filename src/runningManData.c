u32 man_get_height() {
    return height;
}

u32 man_get_width() {
    return width;
}

u32 man_get_frames() {
    return frames;
}

bool man_get_pixel(u32 frame, u32 x, u32 y) {

    if (y >= height || x>= width || frame >=frames) {
        return false;
    }

    if (x<8) {
        return man_data[frame*height + y][0] & (0x80 >> x) ? true : false;
    } else {
        return man_data[frame*height + y][1] & (0x80 >> (x-8)) ? true : false;
    }
}

